# =============================================================
# 세트 메서드
# - 세트는 '중복 없는' 값들의 모음 (순서 없음)
# - 순서가 없으므로 인덱싱/슬라이싱 불가
# - 출력 순서가 작성 순서와 다르게 나오는 것이 정상
# =============================================================

# =============================================================
# [1] add - 항목 하나 추가
# - 이미 있는 값을 다시 추가하면 에러 없이 '아무 일도 일어나지 않음'
#   => 두 번째 add(4)를 해도 결과가 동일 (중복 불가 특성)
# =============================================================

# add
my_set = {'a', 'b', 'c', 1, 2, 3}
my_set.add(4)
print(my_set)

my_set.add(4)
print(my_set)

# =============================================================
# [2] update - 여러 항목을 한 번에 추가
# - 리스트 등 순회 가능한 대상을 풀어서 각 요소를 추가 (리스트의 extend와 유사)
# - 이미 있는 1은 무시되고, 새로운 4와 5만 실제로 추가됨
# =============================================================

# update
my_set = {'a', 'b', 'c', 1, 2, 3}
my_set.update([1, 4, 5])
print(my_set)  # {'c', 2, 3, 1, 'b', 4, 5, 'a'}

# =============================================================
# [3] clear - 모든 항목 제거
# - 빈 세트는 {}가 아니라 set()으로 출력됨 ({}는 빈 딕셔너리)
# =============================================================

# clear
my_set = {'a', 'b', 'c', 1, 2, 3}
my_set.clear()
print(my_set)  # set()

# =============================================================
# [4] remove vs discard - 삭제의 두 가지 방식
# - remove  : 없는 값을 지우려 하면 KeyError 발생
# - discard : 없는 값을 지우려 해도 조용히 넘어감 (에러 없음)
#   => 값의 존재 여부를 확신할 수 없다면 discard가 안전
# =============================================================

# remove
my_set = {'a', 'b', 'c', 1, 2, 3}
my_set.remove(2)
print(my_set)
# my_set.remove(10)  # KeyError: 10

# =============================================================
# [5] pop - 임의의 항목 하나를 꺼내며 제거
# - 세트는 순서가 없으므로 '어떤 값이 나올지 지정할 수 없음'
# - 여기서 '임의(arbitrary)'는 매번 무작위라는 뜻이 아니라
#   내부 저장 구조에 따라 정해지므로 예측하지 말라는 의미
#   (자세한 원리는 99_hash_table.py에서 학습)
# =============================================================

# pop
my_set = {'a', 'b', 'c', 1, 2, 3}
element = my_set.pop()
print(element)
print(my_set)

# -------------------------------------------------------------
# discard: 없는 값(10)을 지워도 에러가 나지 않음
# -------------------------------------------------------------

# discard
my_set = {'a', 'b', 'c', 1, 2, 3}
my_set.discard(2)
print(my_set)
my_set.discard(10)

# =============================================================
# [6] 집합 메서드 - 수학의 집합 연산을 그대로 사용
# - difference   : 차집합   (set1에만 있는 것)        연산자 -
# - intersection : 교집합   (양쪽 모두에 있는 것)      연산자 &
# - union        : 합집합   (둘 중 하나라도 있는 것)   연산자 |
# - issubset     : 부분집합 (set1이 set2에 전부 포함되는가)   연산자 <=
# - issuperset   : 상위집합 (set1이 set2를 전부 포함하는가)   연산자 >=
#   => set1과 set2는 서로 걸치기만 하므로 포함 관계가 아님 => 둘 다 False
#      set3({0,1})은 set1에 완전히 들어 있으므로 issubset이 True
# =============================================================

# 집합 메서드
set1 = {0, 1, 2, 3, 4}
set2 = {1, 3, 5, 7, 9}
set3 = {0, 1}

print(set1.difference(set2))  # {0, 2, 4}
print(set1.intersection(set2))  # {1, 3}
print(set1.issubset(set2))  # False
print(set3.issubset(set1))  # True
print(set1.issuperset(set2))  # False
print(set1.union(set2))  # {0, 1, 2, 3, 4, 5, 7, 9}
