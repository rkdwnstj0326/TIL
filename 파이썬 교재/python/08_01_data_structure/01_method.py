# =============================================================
# 메서드(Method)란?
# - 특정 자료형(타입)에 '소속되어' 있는 함수
# - 그 타입의 데이터가 스스로 할 수 있는 동작이라고 생각하면 쉬움
# - 문법: 데이터.메서드()  =>  '데이터에 점(.)을 찍어' 호출
#
# =============================================================

# =============================================================
# [1] 자료형마다 쓸 수 있는 메서드가 다르다
# - type()으로 그 데이터가 무슨 타입인지 확인
# - help()로 그 타입이 가진 메서드 목록과 설명을 볼 수 있음
#   => str이 가진 메서드와 list가 가진 메서드가 서로 다름
# =============================================================

print(type('1'))  # <class 'str'>
print(type([1, 2]))  # <class 'list'>
print(help(str))
print(help(list))


# =============================================================
# [2] 함수 vs 메서드 - 호출 방식의 차이
# - 함수    : 이름만으로 단독 호출        =>  append()
# - 메서드  : 데이터에 붙여서 호출        =>  리스트.append()
# - 아래 '리스트.append()', '객체.메서드()'는 형태를 보여주는
#   설명용 표기 (실제 실행되는 코드는 아님)
# =============================================================


# 함수
def append():
    pass


# 함수 호출
append()


# 메서드 호출
리스트.append()
객체.메서드()


# =============================================================
# [3] 실제 메서드 호출 예시
# - 문자열도, 리스트도 각자의 메서드를 가지고 있음
# =============================================================

# 문자열 메서드 예시
str_object = 'hello'
print(str_object.capitalize())  # Hello

# 리스트 메서드 예시
numbers = [1, 2, 3]
numbers.append(4)
print(numbers)