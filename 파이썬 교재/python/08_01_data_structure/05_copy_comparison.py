# =============================================================
# 객체와 참조 - 복사를 이해하기 위한 전제
# - 변수 = 값 을 실행하면, 변수는 값 자체가 아니라
#   값이 저장된 '메모리 위치'를 가리킴(참조)
# - b = a 는 값을 복사하는 게 아니라 '같은 위치를 함께 가리키게' 함
# - is 연산자: 두 변수가 '같은 객체'를 가리키는지 확인
#   id() 함수: 객체의 메모리 주소(고유 번호)를 확인
# =============================================================

# =============================================================
# [1] 가변 객체(리스트) - b = a 의 함정
# - b는 a와 같은 리스트를 가리킴 => b를 바꾸면 a도 바뀐 것처럼 보임
# - 사실은 '둘 다 같은 하나의 리스트'라 당연한 결과
#   => a is b 가 True
# =============================================================

# 가변(mutable) 객체 예시
print('가변(mutable) 객체 예시')
a = [1, 2, 3, 4]
b = a
b[0] = 100

print(f'a의 값: {a}')  # [100, 2, 3, 4]
print(f'b의 값: {b}')  # [100, 2, 3, 4]
print(f'a와 b가 같은 객체를 참조하는가? {a is b}')  # True

# =============================================================
# [2] 불변 객체(정수) - 같은 문제가 생기지 않는 이유
# - b = 10 은 b가 '새로운 값 10'을 가리키도록 바꾸는 것
#   => a가 가리키던 20은 그대로, a와 b는 다른 객체
# - 불변 객체는 값을 바꾸는 게 아니라 '다른 값으로 갈아타는' 방식이라
#   원본이 영향을 받지 않음
# =============================================================

# 불변(immutable) 객체 예시
print('\n불변(immutable) 객체 예시')
a = 20
b = a
b = 10

print(f'a의 값: {a}')  # 20
print(f'b의 값: {b}')  # 10
print(a is b)  # False

# =============================================================
# [3] id()로 직접 확인 - 눈으로 참조를 보기
# - x, y는 b = a 관계 => 같은 id (같은 객체)
# - z는 값은 같지만 따로 만든 리스트 => 다른 id (다른 객체)
#   => 값이 같은 것(==)과 같은 객체인 것(is)은 완전히 다른 문제
# =============================================================

# id() 함수를 사용한 메모리 주소 확인
print('\n메모리 주소 확인')
x = [1, 2, 3]
y = x
z = [1, 2, 3]

print(f'x의 id: {id(x)}')
print(f'y의 id: {id(y)}')
print(f'z의 id: {id(z)}')
print(f'x와 y는 같은 객체인가? {x is y}')
print(f'x와 z는 같은 객체인가? {x is z}')
