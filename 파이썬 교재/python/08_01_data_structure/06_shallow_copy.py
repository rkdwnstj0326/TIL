# =============================================================
# 얕은 복사 (Shallow Copy)
# - 앞 파일에서 본 'b = a'의 문제(같은 객체 공유)를 피하는 방법
# - 겉(바깥) 리스트는 새로 만들어 별개의 객체가 됨 => a is b 는 False
# - 방법 3가지: 슬라이싱 a[:], .copy() 메서드, list() 함수 (결과 동일)
# =============================================================

# 얕은 복사
print('\n얕은 복사 예시')

# =============================================================
# [1] 슬라이싱으로 복사 - a[:] 는 전체를 잘라 새 리스트를 만듦
# =============================================================

# 1. 1차원 리스트에서의 얕은 복사 (리스트 슬라이싱)
a = [1, 2, 3]
b = a[:]

print(a)  # [1, 2, 3]
print(b)  # [1, 2, 3]

print(a is b)  # False  => 별개의 객체

# =============================================================
# [2] copy() 메서드로 복사 - 의도가 가장 명확한 방법
# =============================================================

# 2. 1차원 리스트에서의 얕은 복사 (copy 메서드)
a = [1, 2, 3]
b = a.copy()

print(a)  # [1, 2, 3]
print(b)  # [1, 2, 3]

print(a is b)  # False


# =============================================================
# [3] list() 함수로 복사 - 리스트를 새로 만들며 값을 채움
# =============================================================

# 3. 1차원 리스트에서의 얕은 복사 (list() 함수)
a = [1, 2, 3]
b = list(a)

print(a)  # [1, 2, 3]
print(b)  # [1, 2, 3]

print(a is b)  # False


# =============================================================
# [4] 얕은 복사의 한계 - '한 겹'만 복사한다
# - 얕은 복사는 바깥 리스트만 새로 만들고,
#   '안쪽 리스트'는 여전히 원본과 같은 객체를 공유함
# - 그래서 두 가지 결과가 갈림
#   ① b[0] = 999 : 바깥 요소 교체 => a는 영향 없음 (제대로 분리됨)
#   ② b[2][1] = 100 : 안쪽 리스트 수정 => a도 함께 바뀜 (공유 중이라서)
#   => a[2] is b[2] 가 True (안쪽은 같은 객체)
# - 이 한계를 해결하는 것이 다음 파일의 '깊은 복사'
# =============================================================

# 얕은 복사의 한계
print('\n다차원 리스트 얕은 복사의 한계')
a = [1, 2, [3, 4, 5]]
b = a[:]

b[0] = 999
print(a)  # [1, 2, [3, 4, 5]]
print(b)  # [999, 2, [3, 4, 5]]

b[2][1] = 100
print(a)  # [1, 2, [3, 100, 5]]
print(b)  # [999, 2, [3, 100, 5]]

print(f'a[2]와 b[2]가 같은 객체인가? {a[2] is b[2]}')  # True
