################ 리스트 값 추가 및 삭제 ################
# =============================================================
# [중요] 리스트는 가변(mutable)
# - 문자열과 반대로, 아래 메서드들은 '원본 리스트를 직접' 바꿈
# - 그래서 대부분 반환값이 없음(None) => 결과를 변수에 담을 필요 없음
#   my_list.append(4) 로 끝, new = my_list.append(4) 는 실수
# =============================================================

# =============================================================
# append - 맨 뒤에 요소 '하나'를 추가
# - 원본을 바꾸고 None을 반환 => print로 반환값을 찍으면 None
# =============================================================

# append
my_list = [1, 2, 3]
my_list.append(4)
print(my_list)  # [1, 2, 3, 4]
# append는 None을 반환합니다.
print(my_list.append(4))  # None

# =============================================================
# extend vs append - 초보자가 가장 헷갈리는 짝
# - extend : 리스트를 '풀어서' 요소들을 하나씩 이어붙임
# - append : 넘긴 것을 '통째로' 하나의 요소로 넣음
#   => extend([4,5,6]) 은 4,5,6 각각 추가
#   => append([5,6,7]) 은 [5,6,7] 리스트 자체가 하나의 요소로 들어감
# - extend의 대상은 순회 가능해야 함 => extend(100)은 TypeError
# =============================================================

# extend
my_list = [1, 2, 3]
my_list.extend([4, 5, 6])
print(my_list)  # [1, 2, 3, 4, 5, 6]

# extend와 append의 비교
my_list.append([5, 6, 7])
print(my_list)  # [1, 2, 3, 4, 5, 6, [5, 6, 7]]

# my_list.extend(100)  # TypeError: 'int' object is not iterable

# =============================================================
# insert - 원하는 '위치'에 끼워 넣기
# - insert(위치, 값) => 그 위치에 값이 들어가고 뒤는 밀려남
# =============================================================

# insert
my_list = [1, 2, 3]
my_list.insert(1, 5)
print(my_list)  # [1, 5, 2, 3]

# =============================================================
# remove - '값'으로 삭제 (위치가 아님)
# - 처음 만나는 값 하나만 제거 => 뒤의 2들은 그대로 남음
# =============================================================

# remove
my_list = [1, 2, 3, 2, 2, 2]
my_list.remove(2)
print(my_list)  # [1, 3, 2, 2, 2]

# =============================================================
# pop - '위치'로 꺼내면서 삭제 (그 값을 반환함)
# - remove와 달리 pop은 꺼낸 값을 돌려줌
# - 인자 없으면 맨 마지막, 인자를 주면 그 위치
#   => pop()은 5(마지막), pop(0)은 1(맨 앞)
# =============================================================

# pop
my_list = [1, 2, 3, 4, 5]
item1 = my_list.pop()
item2 = my_list.pop(0)

print(item1)  # 5
print(item2)  # 1
print(my_list)  # [2, 3, 4]

# =============================================================
# clear - 모든 요소 제거 (빈 리스트가 됨)
# =============================================================

# clear
my_list = [1, 2, 3]
my_list.clear()
print(my_list)  # []


################ 리스트 정렬 및 순서 변경 ################
# =============================================================
# [공통 주의] reverse와 sort 모두 '원본을 바꾸고 None을 반환'
# - 정렬된 결과를 얻으려고 new = my_list.sort() 하면 new는 None
# - 원본을 지키며 정렬하고 싶다면 내장 함수 sorted()를 사용
#   (메서드 체이닝 파일에서 자세히 다룸)
# =============================================================

# =============================================================
# reverse - 순서를 거꾸로 뒤집기 (정렬 아님)
# - 크기순이 아니라 '현재 순서'를 그대로 반대로 만듦
# =============================================================

# reverse
my_list = [1, 3, 2, 8, 1, 9]
my_list.reverse()
# reverse는 None을 반환합니다.
# print(my_list.reverse())  # None
# reverse는 원본 리스트를 변경합니다.
print(my_list)  # [9, 1, 8, 2, 3, 1]

# =============================================================
# sort - 크기순 정렬 (기본: 오름차순)
# - reverse=True 옵션을 주면 내림차순
# =============================================================

# sort
my_list = [3, 2, 100, 1]
my_list.sort()
# sort는 None을 반환합니다.
# print(my_list.sort())  # None
# sort는 원본 리스트를 변경합니다.
print(my_list)  # [1, 2, 3, 100]

# sort(내림차순 정렬)
my_list.sort(reverse=True)
print(my_list)  # [100, 3, 2, 1]
