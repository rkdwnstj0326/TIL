# =============================================================
# 깊은 복사 (Deep Copy)
# - 얕은 복사의 한계(안쪽 객체 공유)를 완전히 해결
# - 안쪽에 중첩된 리스트/딕셔너리까지 '전부 새로' 만들어 복사
# - copy 모듈의 deepcopy() 함수를 사용 => import copy 필요
# =============================================================

# 깊은 복사
import copy

print('깊은 복사 예시')

# =============================================================
# [1] 중첩 리스트 깊은 복사
# - b는 안쪽 [3,4,5]까지 별개의 객체로 복사됨
# - 그래서 b[2][1] = 100 을 해도 a는 전혀 영향받지 않음
#   => a[2] is b[2] 가 False (얕은 복사에서는 True였던 부분)
# =============================================================

a = [1, 2, [3, 4, 5]]
b = copy.deepcopy(a)

b[2][1] = 100

print(a)  # [1, 2, [3, 4, 5]]
print(b)  # [1, 2, [3, 100, 5]]
print(f'a[2]와 b[2]가 같은 객체인가? {a[2] is b[2]}')  # False

# =============================================================
# [2] 여러 겹 중첩된 복잡한 구조에서도 동일하게 동작
# - 딕셔너리 안의 리스트, 그 안의 또 다른 리스트까지 모두 분리됨
# - 복사본을 아무리 수정해도 원본(original)은 그대로 보존됨
#   => original['b'] is copied['b'] 가 False
# - 정리: 안쪽 데이터까지 독립적으로 다뤄야 한다면 깊은 복사를 사용
# =============================================================

# 복잡한 중첩 객체에서의 깊은 복사 예시
print('복잡한 중첩 객체 깊은 복사')
original = {
    'a': [1, 2, 3],
    'b': {'c': 4, 'd': [5, 6]},
}
copied = copy.deepcopy(original)

copied['a'][1] = 100
copied['b']['d'][0] = 500

print(f'원본: {original}')  # {'a': [1, 2, 3], 'b': {'c': 4, 'd': [5, 6]}}
print(f'복사본: {copied}')  # {'a': [1, 100, 3], 'b': {'c': 4, 'd': [500, 6]}}
print(
    f"original['b']와 copied['b']가 같은 객체인가? {original['b'] is copied['b']}"
)  # False
