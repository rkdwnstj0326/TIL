# =============================================================
# [심화] 메서드 체이닝 (Method Chaining)
# - 메서드 호출 결과에 '점(.)을 이어' 또 다른 메서드를 호출하는 것
# - 앞 메서드가 '값을 반환'해야만 뒤로 이을 수 있음 (핵심 조건)
# - 문자열 메서드는 새 문자열을 반환하므로 체이닝이 잘 됨
#   반면 리스트의 sort/append는 None을 반환해 체이닝 불가 (아래 참고)
# =============================================================

# =============================================================
# [1] 문자열 메서드 체이닝 - 왼쪽에서 오른쪽으로 순서대로 적용
# - swapcase()의 결과 문자열에 다시 replace()를 이어서 적용
# =============================================================

# 문자열 메서드 체이닝
text = 'heLLo, woRld!'
new_text = text.swapcase().replace('l', 'z')
print(new_text)  # HEzzO, WOrLD!


# =============================================================
# [2] 단계별 실행 vs 한 줄 - 완전히 같은 동작
# - 체이닝은 아래 단계별 코드를 압축한 것일 뿐
# - swapcase로 대소문자가 뒤집힌 뒤(l이 소문자로 생김),
#   그 소문자 l이 replace로 z가 됨 => 순서가 결과를 결정
# =============================================================

# 1. 단계별로 실행하기
text = 'heLLo, woRld!'
step1 = text.swapcase()
print('1단계 결과:', step1)  # HEllO, WOrLD!

step2 = step1.replace('l', 'z')
print('2단계 결과:', step2)  # HEzzO, WOrLD!

# 2. 한 줄로 실행하기 (위와 동일한 결과)
new_text = text.swapcase().replace('l', 'z')
print('최종 결과:', new_text)  # HEzzO, WOrLD!


# =============================================================
# [3] 리스트 메서드 체이닝의 함정 - None 반환 문제
#
# [실행 주의] [실수 2] 줄에서 AttributeError로 멈춥니다.
#    확인 후 주석 처리하고 아래 해결책을 실행하세요.
#
# - [실수 1] sort()는 원본만 바꾸고 None을 반환
#            => result에는 정렬 결과가 아닌 None이 담김
# - [실수 2] append()도 None을 반환
#            => None.extend(...)를 호출하는 꼴이라 AttributeError
# =============================================================

# 리스트 메서드 체이닝

# 1. 리스트 메서드 체이닝의 흔한 실수
numbers = [3, 1, 4, 1, 5, 9, 2]

# [실수 1] sort()는 None을 반환하므로, 변수에 아무것도 저장되지 않음
result = numbers.copy().sort()
print(result)  # None (정렬된 리스트를 기대했으나 None 출력)

# [실수 2] append()가 반환한 None 뒤에 메서드를 이을 수 없음
numbers.append(7).extend(
    [8, 9]
)  # AttributeError: 'NoneType' object has no attribute 'extend'


# =============================================================
# [4] 올바른 해결책
# - [해결 1] sorted()는 '새 리스트'를 반환 => 변수에 담기고 체이닝도 가능
#            원본을 안 바꾸므로 copy()도 필요 없음 (추천)
# - [해결 2] 체이닝을 포기하고 한 줄씩 명확하게 작성
#            None 반환 메서드는 이 방식이 정석
# =============================================================

# 2. 올바른 해결책 (Good Cases)
numbers = [3, 1, 4, 1, 5, 9, 2]

# [해결 1] 내장 함수 sorted() 사용 (추천)
# sorted()는 정렬된 '새로운 리스트'를 반환하므로 변수에 할당 가능
# (원본을 건드리지 않으므로 copy()를 굳이 쓸 필요 없음)
sorted_numbers = sorted(numbers)
print(sorted_numbers)  # [1, 1, 2, 3, 4, 5, 9]

# [해결 2] 메서드를 단계별로 나누어 작성
# 체이닝을 포기하고, 명확하게 한 줄씩 실행
copied_list = numbers.copy()
copied_list.sort()
print(copied_list)  # [1, 1, 2, 3, 4, 5, 9]
