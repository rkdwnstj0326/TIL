# =============================================================
# [심화] 리스트 컴프리헨션 (List Comprehension)
# - 반복문으로 리스트를 만드는 과정을 '한 줄'로 압축하는 문법
# - 문법: [표현식 for 변수 in 순회대상]
# - 빈 리스트 생성 + for + append 3단계를 한 줄로 대체
# =============================================================

# =============================================================
# [1] 사용 전 - 전통적인 방식
# - 빈 리스트를 만들고, 반복하며 하나씩 append
# =============================================================

# 사용 전
numbers = [1, 2, 3, 4, 5]
squared_numbers = []

for num in numbers:
    squared_numbers.append(num**2)

print(squared_numbers)


# =============================================================
# [2] 사용 후 - 같은 결과를 한 줄로
# - [num**2 for num in numbers] 를 소리 내 읽으면
#   "numbers의 각 num에 대해 num**2를 모아 리스트로"
# - 아래 주석 처리된 줄처럼 list(...)로도 표현 가능
# =============================================================

# 사용 후
squared_numbers2 = [num**2 for num in numbers]
# squared_numbers2 = list(num**2 for num in numbers)
print(squared_numbers2)


# =============================================================
# [3] 대표 활용 - 2차원 배열(인접행렬) 생성
# - 알고리즘에서 자주 쓰는 격자(grid) 만들기
# - '_' 는 값을 쓰지 않고 '횟수만' 필요할 때 쓰는 관례적 변수명
# - data1, data2는 완전히 같은 결과 (표현만 다름)
#
#  [흔한 함정] [[0] * 5] * 5 로 만들면 안 됨
#    바깥 * 5가 '같은 안쪽 리스트'를 5번 복제해 참조를 공유함
#    (앞서 배운 얕은 복사의 한계와 같은 문제)
#    => 한 행을 바꾸면 모든 행이 함께 바뀜
#    그래서 반드시 for _ in range(5) 로 매번 새 리스트를 만듦
# =============================================================

# List Comprehension 활용 예시
# "2차원 배열 생성 시 (인접행렬 생성 시)"
data1 = [[0] * (5) for _ in range(5)]
print(data1)
# 또는
data2 = [[0 for _ in range(5)] for _ in range(5)]
print(data2)

