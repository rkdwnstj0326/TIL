# =============================================================
# 공통 시퀀스 메서드
# - 시퀀스(순서가 있는 자료형: str, list, tuple)가 공통으로 가진 메서드
# - 문자열이든 리스트든 같은 이름, 같은 방식으로 동작함
# =============================================================

# =============================================================
# [1] index - 값이 '몇 번째'에 있는지 위치를 반환
# - 처음 만나는 위치 하나만 반환 (0부터 시작)
# - 찾는 값이 없으면 에러(ValueError) 발생
#
# =============================================================

# index
text = 'banana'
print(text.index('a'))  # 1

my_list = [1, 2, 3]
print(my_list.index(2))  # 1


# =============================================================
# [2] count - 값이 '몇 개' 들어 있는지 개수를 반환
# - 'banana'에 'a'는 세 번 등장 => 3
# - 없는 값을 세면 에러가 아니라 0을 반환
# =============================================================

# count
text = 'banana'
print(text.count('a'))  # 3

my_list = [1, 2, 2, 3, 3, 3]
print(my_list.count(3))  # 3
