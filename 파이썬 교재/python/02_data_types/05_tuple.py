# =============================================================
# [1] 튜플(tuple) 표현
# - 소괄호 ()로 표현하는 '순서가 있는' 자료형
# - 요소가 1개일 때는 반드시 뒤에 쉼표를 붙여야 함 => (1,)
# - 소괄호를 생략하고 쉼표만으로도 만들 수 있음
# =============================================================

# 튜플 표현
my_tuple_1 = ()
my_tuple_2 = (1,)
my_tuple_3 = (1, 'a', 3, 'b', 5)
my_tuple_4 = 1, 'hello', 3.14, True

my_tuple = (1, 'a', 3, 'b', 5)

# =============================================================
# [2] 튜플도 시퀀스 => 인덱싱 / 슬라이싱 / len() 동일하게 동작
# - 결과가 리스트가 아닌 '튜플'로 반환된다는 점만 다름
# =============================================================

# 인덱싱
print(my_tuple[1])  # a

# 슬라이싱
print(my_tuple[2:4])  # (3, 'b')
print(my_tuple[:3])  # (1, 'a', 3)
print(my_tuple[3:])  # ('b', 5)
print(my_tuple[::2])  # (1, 3, 5)
print(my_tuple[::-1])  # (5, 'b', 3, 'a', 1)

# 길이
print(len(my_tuple))  # 5

# =============================================================
# [3] 튜플은 불변(immutable)
# - 리스트와 가장 큰 차이점
# - 아래 줄의 주석을 해제하면 TypeError 발생
# =============================================================

# 튜플은 불변
# TypeError: 'tuple' object does not support item assignment
# my_tuple[1] = 'z'


# =============================================================
# [4] 튜플의 활용 - 다중 할당 / 값 교환
# - 겉으로는 튜플이 보이지 않지만, 내부적으로 튜플이 만들어지고
#   다시 풀리는(언패킹) 과정을 거침
# =============================================================

# 다중 할당
x, y = 10, 20
print(x)  # 10
print(y)  # 20
# 실제 내부 동작
# (x, y) = (10, 20)

# 값 교환
x, y = 1, 2
x, y = y, x
# 실제 내부 동작
# temp = (y, x)  # 튜플 생성
# x, y = temp  # 튜플 언패킹
# print(x, y)  # 2 1
