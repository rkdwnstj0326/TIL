# =============================================================
# 가변(mutable) vs 불변(immutable)
# - 만들어진 뒤에 '내부 값'을 바꿀 수 있는가에 대한 구분
# - 불변: str, tuple, int, float, bool ...
# - 가변: list, dict, set ...
# =============================================================

# =============================================================
# [1] immutable - 일부만 바꾸려 하면 TypeError
# - 아래 줄은 의도적인 에러 코드
#   => 실행 시 여기서 프로그램이 멈추므로, 확인 후 주석 처리하고
#      [2]번 가변 예시를 이어서 실행할 것
# =============================================================

# immutable (불변)
my_str = 'hello'
my_str[0] = 'z'  # TypeError: 'str' object does not support item assignment

# =============================================================
# [2] mutable - 인덱스로 값을 직접 수정 가능
# =============================================================

# mutable (가변)
my_list = [1, 2, 3]
my_list[0] = 100
print(my_list)  # [100, 2, 3]
