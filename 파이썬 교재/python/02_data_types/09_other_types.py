# =============================================================
# [1] None
# - '값이 없음'을 명시적으로 나타내는 특별한 값
# - 0이나 빈 문자열('')과는 다른, 별개의 자료형
# =============================================================

# None
my_variable = None
print(my_variable)  # None

# =============================================================
# [2] 불리언(bool)
# - True / False 두 가지 값만 가짐 (첫 글자 대문자 주의)
# - 비교 연산의 결과는 항상 불리언으로 반환됨
# - 조건문의 판단 기준이 되므로 매우 중요한 자료형
# =============================================================

# Boolean
is_active = True
is_logged_in = False

print(is_active)  # True
print(is_logged_in)  # False
print(10 > 5)  # True
print(10 == 5)  # False
