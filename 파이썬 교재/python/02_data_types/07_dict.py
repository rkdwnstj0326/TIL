# =============================================================
# [1] 딕셔너리(dict) 표현
# - 중괄호 {}로 표현하며 {키: 값} 쌍으로 데이터를 저장
# - 키(key)는 중복 불가 / 값(value)에는 어떤 자료형이든 담을 수 있음
# =============================================================

# 딕셔너리 표현
my_dict_1 = {}
my_dict_2 = {'key': 'value'}
my_dict_3 = {'apple': 12, 'list': [1, 2, 3]}
print(my_dict_1)  # {}
print(my_dict_2)  # {'key': 'value'}
print(my_dict_3)  # {'apple': 12, 'list': [1, 2, 3]}


# =============================================================
# [2] 값 접근 - 인덱스가 아닌 '키'로 접근
# - 존재하지 않는 키로 접근하면 KeyError 발생
# - 아래 마지막 줄은 의도적인 에러 코드
#   => 실행 시 여기서 프로그램이 멈추므로, 확인 후 주석 처리할 것
# =============================================================

# 딕셔너리는 키에 접근해 값을 얻어냄
my_dict = {'name': '홍길동', 'age': 25}
print(my_dict['name'])  # '홍길동'
print(my_dict['test'])  # KeyError: 'test'


# =============================================================
# [3] 값 추가 및 변경
# - 문법이 동일함: 없는 키면 '추가', 있는 키면 '변경'
# =============================================================

# 딕셔너리 값 추가 및 변경
my_dict = {'apple': 12, 'list': [1, 2, 3]}
# 추가
my_dict['banana'] = 50
print(my_dict)  # {'apple': 12, 'list': [1, 2, 3], 'banana': 50}

# 변경
my_dict['apple'] = 100
print(my_dict)  # {'apple': 100, 'list': [1, 2, 3], 'banana': 50}
