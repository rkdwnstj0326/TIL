# =============================================================
# [1] 세트(set) 표현
# - 중괄호 {}로 표현하는 '중복 없는' 자료형 (순서 없음)
# - 빈 세트는 {}가 아니라 반드시 set()으로 생성 => {}는 빈 딕셔너리
# - 중복된 값을 넣어도 하나만 남음
# =============================================================

# 세트 표현
my_set_1 = set()
my_set_2 = {1, 2, 3}
my_set_3 = {1, 1, 1}
print(my_set_1)  # set()
print(my_set_2)  # {1, 2, 3}
print(my_set_3)  # {1}


# =============================================================
# [2] 세트의 집합 연산
# - 수학의 집합 연산을 기호로 바로 사용할 수 있음
# - | 합집합 / - 차집합 / & 교집합
# =============================================================

# 세트의 집합 연산
my_set_1 = {1, 2, 3}
my_set_2 = {3, 6, 9}

# 합집합
print(my_set_1 | my_set_2)  # {1, 2, 3, 6, 9}

# 차집합
print(my_set_1 - my_set_2)  # {1, 2}

# 교집합
print(my_set_1 & my_set_2)  # {3}
