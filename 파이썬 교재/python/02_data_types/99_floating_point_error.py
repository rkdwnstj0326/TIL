# =============================================================
# [심화] 부동소수점 오차 (Floating Point Error)
# - 컴퓨터는 실수를 2진수 '근사값'으로 저장하기 때문에
#   10진수 소수를 100% 정확하게 표현하지 못함
# - 지금은 '이런 현상이 있다'는 사실만 인지하면 충분
# =============================================================

# =============================================================
# [1] 대표적인 예시 - 0.1 + 0.2 != 0.3
# =============================================================

# 부동소수점 에러
result = 0.1 + 0.2
print(result == 0.3)  # False
print(result)  # 0.30000000000000004


# =============================================================
# [2] 논리적으로 같아야 할 두 값이 다르게 나오는 경우
# - 수학적으로는 둘 다 0.1이지만 미세한 오차가 생김
# =============================================================

# 해결 전
a = 3.2 - 3.1
b = 1.2 - 1.1

print(a)  # 0.10000000000000009
print(b)  # 0.09999999999999987
print(a == b)  # False

# =============================================================
# [3] 해결 방법 - Decimal 모듈
# - 금융 계산 등 높은 정밀도가 필요할 때 사용하는 전문 도구
# - 주의: Decimal에는 실수가 아닌 '문자열'을 넘겨야 정확함
# =============================================================

# 해결 후
from decimal import Decimal

a = Decimal('3.2') - Decimal('3.1')
b = Decimal('1.2') - Decimal('1.1')

print(a)  # 0.1
print(b)  # 0.1
print(a == b)  # True
