# =============================================================
# [심화] 진법 표현 (Numeral System)
# - 접두사를 붙이면 10진수가 아닌 다른 진법으로 값을 작성할 수 있음
#   0b => 2진수 / 0o => 8진수 / 0x => 16진수
# - print()로 출력하면 항상 10진수로 변환되어 보임
# =============================================================

# 2진수 10은 10진수로 2입니다. (1 * 2^1 + 0 * 2^0)
print(0b10)

# 8진수 30은 10진수로 24입니다. (3 * 8^1 + 0 * 8^0)
print(0o30)

# 16진수 10은 10진수로 16입니다. (1 * 16^1 + 0 * 16^0)
print(0x10)
