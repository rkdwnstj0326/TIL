# =============================================================
# [1] 문자열(str) 표현
# - 작은따옴표(') 또는 큰따옴표(")로 감싼 문자들의 나열
# - type() : 값의 자료형을 확인하는 내장 함수
# =============================================================

# 문자열 표현
print('Hello, World!')  # Hello, World!
print(type('Hello, World!'))  # str

# =============================================================
# [2] 중첩 따옴표
# - 문자열 안에 따옴표를 넣고 싶을 때는 '다른 종류'의 따옴표로 감싼다
# =============================================================

# 중첩 따옴표
print(
    '문자열 안에 "큰따옴표"를 사용하려면 작은 따옴표로 묶는다.'
)  # 문자열 안에 "큰따옴표"를 사용하려면 작은 따옴표로 묶는다.
print(
    "문자열 안에 '작은따옴표'를 사용하려면 큰따옴표로 묶는다."
)  # 문자열 안에 '작은따옴표'를 사용하려면 큰따옴표로 묶는다.

# =============================================================
# [3] 이스케이프 시퀀스 (Escape Sequence)
# - 역슬래시(\)를 붙여 특수한 의미를 부여하거나, 특수 의미를 없앰
# - 자주 쓰는 것: \n(줄바꿈), \t(탭), \'(작은따옴표), \\(역슬래시)
# =============================================================

# Escape sequence
# 따옴표 앞에 \를 붙여 문자로 인식시킴
print('He\'s a boy.')
# \n 은 줄바꿈(엔터)을 의미함
print('첫째 줄\n둘째 줄')

# =============================================================
# [4] 여러 줄 문자열
# - 삼중 따옴표(""" 또는 ''')로 감싸면 줄바꿈이 그대로 유지됨
# =============================================================

# 여러 줄 문자열을 작성할 때는 """ 또는 '''를 사용
multi_line_str = """
이것은
여러 줄로 이루어진
문자열입니다.
"""
print(multi_line_str)


# =============================================================
# [5] f-string (문자열 보간, String Interpolation)
# - 문자열 앞에 f를 붙이면 중괄호 {} 안의 변수/식이 값으로 치환됨
# - 실무에서 가장 많이 쓰는 문자열 조합 방식
# =============================================================

# String Interpolation "f-string"
name = '홍길동'
age = 25
greeting = f'안녕하세요, 제 이름은 {name}이고 나이는 {age}살입니다.'
# 안녕하세요, 제 이름은 홍길동이고 나이는 25살입니다.
print(greeting)


# =============================================================
# [6] 문자열의 시퀀스(Sequence) 특징
# - 순서가 있는 자료형 => 인덱싱 / 슬라이싱 / len() 사용 가능
# - 인덱스는 0부터 시작, 음수 인덱스는 뒤에서부터
# - 슬라이싱 [시작:끝:간격] => '끝' 인덱스는 포함되지 않음
# =============================================================

# 문자열의 시퀀스 특징
my_str = 'hello'
# 1. 인덱싱
print(my_str[1])  # e


# 2. 슬라이싱
print(my_str[2:4])  # ll
print(my_str[:3])  # hel
print(my_str[3:])  # lo
print(my_str[::2])  # hlo
print(my_str[::-1])  # olleh

# 3. 길이
print(len(my_str))  # 5

# =============================================================
# [7] 문자열은 불변(immutable)
# - 한 번 만들어진 문자열은 일부만 바꿀 수 없음
# - 아래 줄은 의도적으로 에러를 발생시키는 코드
#   => 실행 시 여기서 프로그램이 멈추므로, 확인 후 주석 처리할 것
# =============================================================

# 4. 문자열은 불변
# TypeError: 'str' object does not support item assignment
my_str[1] = 'z'
