# =============================================================
# [1] 리스트(list) 표현
# - 대괄호 []로 표현하는 '순서가 있는' 자료형
# - 서로 다른 자료형을 함께 담을 수 있고, 리스트 안에 리스트도 가능
# =============================================================

# 리스트 표현
my_list_1 = []
my_list_2 = [1, 'a', 3, 'b', 5]
my_list_3 = [1, 2, 3, 'Python', ['hello', 'world', '!!!']]
my_list = [1, 'a', 3, 'b', 5]

# =============================================================
# [2] 리스트도 시퀀스 => 인덱싱 / 슬라이싱 / len() 동일하게 동작
# - 문자열에서 배운 규칙이 그대로 적용됨
# =============================================================

# 인덱싱
print(my_list[1])  # a

# 슬라이싱
print(my_list[2:4])  # [3, 'b']
print(my_list[:3])  # [1, 'a', 3]
print(my_list[3:])  # ['b', 5]
print(my_list[::2])  # [1, 3, 5]
print(my_list[::-1])  # [5, 'b', 3, 'a', 1]

# 길이
print(len(my_list))  # 5

# =============================================================
# [3] 중첩 리스트 접근
# - 대괄호를 연달아 사용해 안쪽으로 한 단계씩 들어감
# - my_list[-1][1][0] : 마지막 요소 => 그 안의 1번 => 그 안의 0번 글자
# =============================================================

# 중첩된 리스트 접근
my_list = [1, 2, 3, 'Python', ['hello', 'world', '!!!']]
print(len(my_list))  # 5
print(my_list[4][-1])  # !!!
print(my_list[-1][1][0])  # w

# =============================================================
# [4] 리스트는 가변(mutable)
# - 문자열과 달리 만들어진 뒤에도 내부 값을 수정할 수 있음
# =============================================================

# 리스트는 가변
# 1. 인덱싱으로 값 수정
my_list = [1, 2, 3, 4, 5]
my_list[1] = 'two'
print(my_list)  # [1, 'two', 3, 4, 5]

# 2. 슬라이싱으로 값 수정
my_list = [1, 2, 3, 4, 5]
my_list[2:4] = ['three', 'four']
print(my_list)  # [1, 2, 'three', 'four', 5]
