# =============================================================
# [1] 암시적 형변환 (Implicit Type Conversion)
# - 개발자가 지시하지 않아도 파이썬이 알아서 자료형을 맞춰주는 것
# - 표현 범위가 더 넓은 쪽으로 변환됨 (bool => int => float)
# - True는 1, False는 0으로 취급됨
# =============================================================

# 암시적 형변환
# 정수(int)와 실수(float)의 덧셈
print(3 + 5.0)  # 8.0
# 불리언(bool)과 정수(int)의 덧셈
print(True + 3)  # 4
# 불리언간의 덧셈
print(True + False)  # 1

# =============================================================
# [2] 명시적 형변환 (Explicit Type Conversion)
# - 개발자가 int() / float() / str() 등으로 직접 지시하는 것
# - 주의: int('3.5')는 에러 (문자열은 정수 형태만 변환 가능)
#         int(3.5)는 소수점 아래를 버리고 3
# =============================================================

# 명시적 형변환
# str -> int
print(int('1'))  # 1
# ValueError: invalid literal for int() with base 10: '3.5'
# print(int('3.5'))
print(int(3.5))  # 3
print(float('3.5'))  # 3.5

# int -> str
print(str(1) + '등')  # 1등
