# =============================================================
# map 함수 - 모든 요소에 같은 함수를 한 번에 적용
# - 문법: map(적용할함수, 순회할대상)
# - 반복문으로 하나씩 처리하던 일을 한 줄로 표현할 수 있음
#
# ⚠️ [실행 주의] [2]번은 input()으로 사용자 입력을 기다립니다.
#    터미널에서 실행하고, 숫자 사이를 공백으로 띄워 입력하세요. (예: 1 2 3)
# =============================================================

# =============================================================
# [1] map 기본 - 결과가 바로 보이지 않는 이유
# - map()은 결과를 미리 만들지 않고 '준비만' 해둠 (range와 유사)
#   => 그래서 print(result)는 <map object ...>로 출력됨
# - 실제 값을 보려면 list()로 형 변환해야 함
# - 주의: map 결과는 한 번 꺼내 쓰면 소진됨
#         list(result)를 두 번 호출하면 두 번째는 빈 리스트
# =============================================================

# map 함수 사용 기본
numbers = [1, 2, 3]
result = map(str, numbers)

print(result)  # <map object at 0x00000239C915D760>
print(list(result))  # ['1', '2', '3']


# =============================================================
# [2] 활용 1 - input()과 함께 쓰는 정석 패턴
# - input()이 받은 값은 항상 '문자열'
# - .split() : 공백을 기준으로 잘라 리스트로 만듦
#              => 다만 요소가 여전히 문자열이라 계산에 쓸 수 없음
# - map(int, ...) : 모든 요소를 정수로 한 번에 변환
# - 알고리즘 문제 풀이에서 가장 많이 쓰는 형태
# =============================================================

# map 함수 활용 1 - input과 함께 사용
# 터미널 창에서 1 2 3 입력 (공백 주의)
numbers1 = input().split()
print(numbers1)  # ['1', '2', '3']

## 터미널 창에서 1 2 3 입력 (공백 주의)
numbers2 = list(map(int, input().split()))
print(numbers2)  # [1, 2, 3]


# =============================================================
# [3] 활용 2 - lambda와 함께 사용
# - map의 첫 인자는 '함수 자체' => 괄호 없이 이름만 전달 (square)
# - 한 번만 쓸 간단한 함수라면 lambda로 즉석에서 만들어 넘기는 편이 간결
# - 두 방식의 결과는 완전히 동일함
# =============================================================

# map 함수 활용 2 - lambda와 함께 사용
numbers = [1, 2, 3, 4, 5]


def square(x):
    return x**2


# lambda 미사용
squared1 = list(map(square, numbers))
print(squared1)  # [1, 4, 9, 16, 25]

# lambda 사용
squared2 = list(map(lambda x: x**2, numbers))
print(squared2)  # [1, 4, 9, 16, 25]
