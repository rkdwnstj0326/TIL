# =============================================================
# [2] 반복 변수의 정체
# - 반복 변수(item)의 이름은 자유롭게 정할 수 있음
# - 매 반복마다 다음 요소가 자동으로 재할당됨
# =============================================================

# for문 작동 원리
item_list = ['apple', 'banana', 'coconut']

for item in item_list:  # item: 반복 변수
    print(item)


# =============================================================
# [3] 문자열 순회 - 문자열도 순회 가능한 대상
# - 한 글자씩 꺼내짐 (K => o => r => e => a)
# =============================================================

# 문자열 순회
country = 'Korea'

for char in country:
    print(char)


# =============================================================
# [4] range 순회 - 정해진 '횟수'만큼 반복할 때
# - 꺼낼 데이터가 없고 반복 횟수만 필요할 때 사용
# =============================================================

# range 순회
for i in range(5):
    print(i)


# =============================================================
# [5] 딕셔너리 순회
# - 딕셔너리를 그냥 순회하면 '키'가 꺼내짐 (값이 아님)
# - 값이 필요하면 my_dict[key]로 다시 접근
#   => .items()를 쓰면 키와 값을 한 번에 받을 수 있음 (앞서 학습)
# =============================================================

# for문 dictionary 순회
my_dict = {
    'x': 10,
    'y': 20,
    'z': 30,
}

for key in my_dict:
    print(key)
    print(my_dict[key])


# =============================================================
# [6] 인덱스 순회 - 원본 리스트의 값을 '수정'해야 할 때
# - for num in numbers 방식은 값의 복사본을 꺼내오므로
#   num을 바꿔도 원본 리스트는 변하지 않음
# - 원본을 바꾸려면 위치(인덱스)로 접근해야 함
#   => range(len(numbers))로 0 ~ 마지막 인덱스를 만들어 사용
# =============================================================

# 인덱스 순회
numbers = [4, 6, 10, -8, 5]

for i in range(len(numbers)):
    numbers[i] = numbers[i] * 2

print(numbers)


# =============================================================
# [7] 중첩 반복문 - 반복 안의 반복
# - 바깥 1회당 안쪽이 전부 돌고 나서 바깥이 다음으로 넘어감
# - 총 실행 횟수 = 바깥 횟수 x 안쪽 횟수 (2 x 2 = 4회)
#   => A c / A d / B c / B d 순서
# =============================================================

# 중첩 반복문
outers = ['A', 'B']
inners = ['c', 'd']

for outer in outers:
    for inner in inners:
        print(outer, inner)


# =============================================================
# [8] 중첩 리스트 순회 - 한 겹 벗길 것인가, 두 겹 벗길 것인가
# - 1: 한 번만 순회 => 안쪽 '리스트' 자체가 꺼내짐 (['A', 'B'])
# - 2: 두 번 순회 => 리스트 안의 '요소'까지 꺼내짐 (A, B, c, d)
# =============================================================

# 중첩 리스트 순회
elements = [['A', 'B'], ['c', 'd']]

# 1
for elem in elements:
    print(elem)

# 2
for elem in elements:
    for item in elem:
        print(item)


# =============================================================
# [9] for와 while 비교
# - for   : 반복할 '대상'이 있고, 횟수가 정해져 있을 때
# - while : 반복을 멈출 '조건'이 있고, 횟수를 모를 때
#   => 아래 두 코드는 모두 3번 반복하지만 사고 방식이 다름
# =============================================================

for i in range(3):
    print('반짝')

count = 1

while count <= 3:
    print(count)
    count = count + 1

print('끝')
