# =============================================================
# [심화] zip 함수 - 여러 개의 순회 대상을 같은 위치끼리 묶기
# - 문법: zip(대상1, 대상2, ...)
# - 각 대상의 0번끼리, 1번끼리 짝지어 튜플로 만들어 줌
# - 길이가 다르면 '가장 짧은 것'을 기준으로 잘림 (에러 아님)
# =============================================================

# =============================================================
# [1] zip 기본 - map과 마찬가지로 결과가 바로 보이지 않음
# - zip()도 결과를 미리 만들지 않으므로 <zip object ...>로 출력됨
# - 실제 값을 보려면 list()로 형 변환
# - 주의: 한 번 꺼내 쓰면 소진됨
#         list(pair)를 다시 호출하면 빈 리스트가 나옴
# =============================================================

# zip 함수 사용 기본
girls = ['jane', 'ashley']
boys = ['peter', 'jay']
pair = zip(girls, boys)

print(pair)  # <zip object at 0x000001C76DE58700>
print(list(pair))  # [('jane', 'peter'), ('ashley', 'jay')]


# =============================================================
# [2] 활용 - 여러 리스트를 나란히 순회
# - 세 과목 점수가 '학생 순서'로 정렬되어 있다는 전제
#   => 0번 학생의 (국어, 수학, 영어)가 한 튜플로 묶임
# - 인덱스를 직접 다루지 않아도 되므로 실수가 줄어듦
# =============================================================

# zip 함수 활용
kr_scores = [10, 20, 30, 50]
math_scores = [20, 40, 50, 70]
en_scores = [40, 20, 30, 50]

for student_scores in zip(kr_scores, math_scores, en_scores):
    print(student_scores)

# =============================================================
# [3] 활용 - 전치 행렬 (행과 열 바꾸기)
# - *scores : 리스트를 언패킹해 각 행을 개별 인자로 전달
#             => zip([10,20,30], [40,50,39], [20,40,50]) 과 동일
# - 결과적으로 '각 행의 0번끼리' 묶이므로 세로 방향으로 읽히게 됨
#   원본 행: (10,20,30) (40,50,39) (20,40,50)
#   결과 열: (10,40,20) (20,50,40) (30,39,50)
# - 앞서 배운 언패킹(*)이 실제로 쓰이는 대표 사례
# =============================================================

# zip 함수 활용 (전치 행렬)
scores = [
    [10, 20, 30],
    [40, 50, 39],
    [20, 40, 50],
]

for score in zip(*scores):
    print(score)
