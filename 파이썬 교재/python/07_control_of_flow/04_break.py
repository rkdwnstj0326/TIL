# [2] break의 대표 용도 - '찾으면 그만 두기'
# - 조건에 맞는 첫 요소를 찾은 뒤 남은 데이터를 볼 이유가 없을 때
# - '확인 중...' 출력을 보면 6에서 멈추고 7, 9, 10, 11은
#   아예 검사하지 않는다는 것을 눈으로 확인할 수 있음
#   => break가 없으면 리스트 끝까지 불필요하게 순회함
# =============================================================

# break 키워드 예시 (for문)
# 리스트에서 첫번째 짝수만 찾은 후 반복 종료하기
numbers = [1, 3, 5, 6, 7, 9, 10, 11]

for num in numbers:
    print('확인 중...', num)
    if num % 2 == 0:
        print('첫 번째 짝수를 찾았습니다:', num)
        break


# =============================================================
# [3] while문에서의 break - 반복 조건 외에 '탈출구'를 하나 더 만들기
# - 이 반복문을 빠져나가는 길은 두 가지
#   ① number가 양수가 되어 while 조건이 False가 됨
#   ② 0을 입력해 break로 즉시 탈출
# - 즉 break는 "조건과 별개인 종료 사유"를 표현할 때 유용함
# =============================================================

# break 키워드 예시 (while문)
# 프로그램 종료 조건 만들기
number = int(input('양의 정수를 입력해주세요. (종료: 0): '))

while number <= 0:
    if number == 0:
        print('프로그램을 종료합니다.')
        break
    print('음수는 입력할 수 없습니다.')
    number = int(input('양의 정수를 입력해주세요. (종료: 0): '))

print('반복문을 빠져나왔습니다.')
