# =============================================================
# while문 (while loop)
# - 조건이 True인 동안 계속 반복
# - for문과 달리 반복 횟수가 미리 정해져 있지 않음
#
# ⚠️ [실행 주의] [2]번은 input()으로 사용자 입력을 기다립니다.
#    터미널에서 실행해야 하며, 입력 없이는 다음으로 진행되지 않습니다.
# =============================================================

# =============================================================
# [1] while문 기본
# - 반드시 필요한 3요소
#   ① 시작 값 (count = 1)
#   ② 반복 조건 (count <= 3)
#   ③ 조건을 변화시키는 코드 (count = count + 1)
# - ③을 빠뜨리면 조건이 영원히 True => 무한 루프
#   (실행이 멈추지 않으면 터미널에서 Ctrl + C로 강제 종료)
# =============================================================

# while문 기본
count = 1

while count <=3:
#3보다 커질 때 가지 돈다.
    print(count)
    count = count +1


print('끝')


# =============================================================
# [2] while문의 대표 용도 - 올바른 입력을 받을 때까지 반복
# - 사용자가 몇 번 만에 제대로 입력할지 알 수 없음 => 횟수를 모름
#   => 이런 상황이 for문이 아닌 while문을 쓰는 이유
# - 흐름
#   ① 먼저 한 번 입력받음
#   ② 조건(number <= 0)에 걸리면 반복 블록으로 진입
#   ③ 블록 마지막에서 '다시 입력'을 받아 조건을 바꿔줌
#      => 이 줄이 없으면 같은 값으로 무한 반복됨
# - 반복 안의 if-else는 잘못된 이유를 구분해 안내하는 역할
# =============================================================

# while문 사용자 입력에 따른 반복
#input 사용자의 입력을 받음

number = int(input('양의 정수를 입력해주세요.: '))

while number <= 0:
    if number < 0:
        print('음수를 입력했습니다.')
    else:
        print('0은 양의 정수가 아닙니다.')

    number = int(input('양의 정수를 입력해주세요.: '))

print('잘했습니다!')
