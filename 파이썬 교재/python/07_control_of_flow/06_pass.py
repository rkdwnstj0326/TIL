# =============================================================
# pass 키워드 - '아무것도 하지 않는다'는 것을 명시
# - 파이썬은 콜론(:) 뒤에 반드시 들여쓰기된 코드가 있어야 함
#   => 비워두면 IndentationError 발생
# - 아직 내용을 정하지 않았지만 구조부터 잡아둘 때 자리를 채우는 용도
# - 주석과의 차이: 주석은 코드가 아니므로 빈 블록 문제를 해결하지 못함
#
# [실행 주의] 이 파일은 실행용이 아니라 '구조를 보는' 예시입니다.
#    condition, condition1, condition2는 정의되지 않은 이름이므로
#    실행하면 첫 블록에서 NameError가 발생합니다.
#    => 코드를 눈으로 읽으며 pass의 위치와 역할만 확인할 것
# =============================================================

# =============================================================
# [1] while문 안에서 - 특정 경우에만 아무 처리도 하지 않음
# - condition2일 때는 의도적으로 넘어가겠다는 표시
# =============================================================

# pass 키워드 예시 (while 문)
while True:
    if condition1:
        break
    elif condition2:
        pass  # 빈 코드를 의미
    else:
        print('출력')

# =============================================================
# [2] if문 안에서 - 분기 구조를 먼저 설계할 때
# - 조건 분기는 확정됐지만 각 처리 내용은 아직 미정인 상태
# =============================================================

# pass 키워드 예시 (if 문)
if condition:
    pass  # 아무런 동작도 수행하지 않음
else:
    pass  # 구조를 잡을 뿐


