# =============================================================
# [심화] for-else 구문
# - for에 붙는 else는 조건문의 else와 의미가 전혀 다름
# - 실행 규칙: break 없이 반복이 끝까지 돌았을 때만 else 실행
#              => break로 중간에 빠져나오면 else는 건너뜀
# - 기억법: else보다 'nobreak'라고 읽으면 헷갈리지 않음
# - 용도: '끝까지 찾아봤지만 없었다'를 표현할 때
# =============================================================

# =============================================================
# [1] for-else 기본
# - 리스트에 짝수가 하나도 없어 break가 실행되지 않음
#   => 반복이 자연스럽게 끝남 => else 실행
# =============================================================

# for-else 구문 기본
numbers = [1, 3, 5, 7, 9]

for num in numbers:
    if num % 2 == 0:
        print('짝수를 찾았습니다:', num)
        break
else:
    # break 없이 끝까지 돌았을 때만 실행
    print('짝수를 찾지 못했습니다')


# =============================================================
# [2] 활용 1 - 찾은 경우 (break 발생)
# - 'guest'를 만나 break => else 블록은 실행되지 않음
# - 마지막 print는 for-else 밖이므로 어느 경우든 항상 실행됨
# =============================================================

# for-else 구문 활용 1
# 중복 아이디 찾기 - 찾은 경우
registered_ids = ['admin', 'user01', 'guest', 'user02']
id_to_check = 'guest'  # 이미 리스트에 존재하는 아이디

for existing_id in registered_ids:
    if existing_id == id_to_check:
        print('이미 사용 중인 아이디입니다.')
        break  # 중복 아이디를 찾았으므로 확인 절차를 중단
else:
    # for 루프가 break로 중단되었기에 이 부분은 실행되지 않음
    print('사용 가능한 아이디입니다.')

print('아이디 확인 절차를 종료합니다.')


# =============================================================
# [3] 활용 2 - 찾지 못한 경우 (break 미발생)
# - [2]번과 코드 구조는 완전히 동일하고 검사할 값만 다름
# - 리스트 끝까지 일치하는 아이디가 없어 break가 실행되지 않음
#   => else 실행 => '사용 가능한 아이디입니다.'
# =============================================================

# for-else 구문 활용 2
# 중복 아이디 찾기 - 찾지 못한 경우
registered_ids = ['admin', 'user01', 'guest', 'user02']
id_to_check = 'new_user'  # 리스트에 없는 새로운 아이디

for existing_id in registered_ids:
    if existing_id == id_to_check:
        print('이미 사용 중인 아이디입니다.')
        break
else:
    # for 루프가 break 없이 마무리 되어 else 블록 실행
    print('사용 가능한 아이디입니다.')
