# =============================================================
# [심화] enumerate 함수 - 인덱스와 값을 함께 꺼내기
# - 문법: enumerate(순회할대상, start=시작번호)
# - range(len(...))로 인덱스를 만들고 다시 접근하던 방식보다 간결하고 안전
#   => for i in range(len(fruits)): print(i, fruits[i])  와 같은 결과
# - 반복 변수를 2개 쓰는 이유는 (인덱스, 값) 튜플이 자동 언패킹되기 때문
# =============================================================

# =============================================================
# [1] enumerate 기본
# - 인덱스는 기본적으로 0부터 시작
# =============================================================

# enumerate 함수 기본
fruits = ['apple', 'banana', 'cherry']

for index, fruit in enumerate(fruits):
    print(index, fruit)
"""
0 apple
1 banana
2 cherry
"""

# =============================================================
# [2] 활용 1 - start 인자로 시작 번호 바꾸기
# - 순위, 목록 번호처럼 사람이 읽는 번호는 보통 1부터 시작
# - 주의: start는 '보여주는 번호'만 바꿀 뿐,
#         실제 데이터의 위치(인덱스)를 바꾸지는 않음
# =============================================================

# enumerate 함수 활용 1
# start 인자를 사용하여 인덱스 번호를 1부터 출력
movies = ['인터스텔라', '기생충', '인사이드 아웃', '라라랜드']

for idx, title in enumerate(movies, start=1):
    print(f'{idx}위: {title}')


# =============================================================
# [3] 활용 2 - 인덱스로 '다른 리스트'의 같은 위치를 참조
# - 두 리스트의 순서가 서로 짝지어져 있을 때 유용
#   answers[0]이 비었으면 respondents[0]이 미제출자
# - 인덱스가 필요한 대표적인 상황
#   => 값만 필요하면 enumerate 없이 순회하는 편이 더 깔끔함
# =============================================================

# enumerate 함수 활용 2
# 인덱스 정보를 활용하여 특정 조건에 맞는 요소 찾기
respondents = ['은지', '정우', '소민', '태호']
answers = ['', '좋아요', '', '괜찮아요']

for i, response in enumerate(answers):
    if response == '':
        print(f'{respondents[i]} 미제출')
