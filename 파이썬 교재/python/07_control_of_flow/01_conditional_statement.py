# =============================================================
# 조건문 (Conditional Statement)
# - 조건이 True일 때만 해당 블록(들여쓰기된 부분)을 실행
# - 콜론(:)과 들여쓰기로 블록의 범위가 결정됨
# =============================================================

# =============================================================
# [1] if 단독 - 조건에 맞을 때만 무언가를 '덧붙임'
# - 조건이 False면 블록을 건너뛰고 다음 줄로 진행
# - print('오늘 기온:', ...)은 조건과 무관하게 항상 실행됨
#   => 들여쓰기 밖에 있기 때문
# =============================================================

## if 단독 - 특정 상황에만 무언가를 덧붙일 때
temperature = 33
#현재 기온이 30도를 넘으면, 폭염주의포 print하겟다
if temperature >= 30:
    print('폭염주의보')

print('오늘 기온:', temperature)


# =============================================================
# [2] if-else - 반드시 둘 중 하나만 실행
# - else에는 조건을 쓰지 않음 (앞의 조건이 아닌 나머지 전부)
# =============================================================

# if-else - 반드시 둘 중 하나
age = 17
#만약에 나이가 19세 이상이라면, print(입점가능)하겟다
if age >= 19:
    print('입장가능')

#19세 이상 아니면 입장 불가 프린트 하겟음
else:
    print('입장불가')


# =============================================================
# [3] if-elif (else 없음) - 해당 없으면 아무 일도 하지 않음
# - elif는 '앞의 조건이 모두 False일 때' 검사됨
# - rank가 5이므로 세 조건 모두 False => 출력 없이 통과
# =============================================================

# if-elif (else 없음)
# 해당 없으면 아무 일도 안 함
rank = 5

if rank == 1:
    print('금메달')
elif rank == 2:
    print('은메달')
elif rank == 3:
    print('동메달')

print('경기 종료')


# =============================================================
# [4] if-elif-else - 모든 경우를 빠짐없이 처리
# - else가 있으면 어떤 값이 와도 반드시 하나는 실행됨
# - 조건 안에 print가 아닌 '변수 할당'을 넣어도 됨
#   => 어떤 가지를 타든 grade에 값이 담기므로 아래에서 안전하게 출력 가능
# =============================================================

# if-elif-else - 모든 경우를 빠짐없이 처리
score = 85

if score >= 90:
    grade = 'A'
elif score >= 80:
    grade = 'B'
elif score >= 70:
    grade = 'C'
else:
    grade = 'F'

print(grade)  # B


# =============================================================
# [5] 복수 조건문 - 조건의 '순서'가 결과를 바꾼다
# - elif는 위에서부터 순서대로 검사하고, 처음 True를 만나면 멈춤
# - 아래 두 코드는 조건도 같고 값도 같지만 순서만 다름
#   => 순서 1: 좁은 범위(150)부터 검사 => '매우 나쁨' (의도한 결과)
#   => 순서 2: 넓은 범위(30)부터 검사 => 155도 30보다 크므로 '보통'에서 멈춤
# - 규칙: 범위 조건은 '가장 좁은(엄격한) 것부터' 위에 쓴다
# =============================================================

# 복수 조건문
## 순서 1. 결과: 매우 나쁨
dust = 155

if dust > 150:
    print('매우 나쁨')
elif dust > 80:
    print('나쁨')
elif dust > 30:
    print('보통')
else:
    print('좋음')


## 순서 2. # 결과: 보통
dust = 155

if dust > 30:
    print('보통')
elif dust > 80:
    print('나쁨')
elif dust > 150:
    print('매우 나쁨')
else:
    print('좋음')


# =============================================================
# [6] 독립 if 여러 개 vs elif - 서로 다른 구조
# - A. 독립된 if 3개  : 각각 따로 검사 => 조건이 겹치면 여러 개가 모두 실행
#                       => 합격 / 우수 / 최우수 3줄 모두 출력
# - B. if-elif 연결   : 하나로 묶인 검사 => 처음 True 하나만 실행
#                       => '합격'에서 멈추고 끝 (1줄만 출력)
# - 어느 쪽이 옳은 게 아니라, 목적에 따라 골라 써야 함
#   "여러 개가 동시에 참일 수 있는가"가 판단 기준
# =============================================================

# 독립 if 여러 개 vs elif
score = 95

# A. 독립된 if 세 개
if score >= 60:
    print('합격')
if score >= 80:
    print('우수')
if score >= 90:
    print('최우수')


score = 95

# B. if - elif 로 연결
if score >= 60:
    print('합격')
elif score >= 80:
    print('우수')
elif score >= 90:
    print('최우수')


# =============================================================
# [7] 중첩 조건문 - 조건 안에 조건
# - 바깥 조건이 True일 때만 안쪽 조건을 검사함
# - 들여쓰기 단계가 곧 조건의 깊이
#   => dust > 150 통과 후, 그 안에서 dust > 300을 다시 검사
# =============================================================

# 중첩 조건문 동작 예시
# 출력: 매우 나쁨
#      위험해요! 나가지 마세요!
dust = 480

if dust > 150:
    print('매우 나쁨')
    if dust > 300:
        print('위험해요! 나가지 마세요!')
elif dust > 80:
    print('나쁨')
elif dust > 30:
    print('보통')
else:
    print('좋음')
