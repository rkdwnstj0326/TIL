# =============================================================
# 패킹 (Packing) - 여러 개의 값을 하나로 묶는 것
# =============================================================

# =============================================================
# [1] 기본 패킹
# - 쉼표로 나열된 값들은 자동으로 '튜플'로 묶임
# - 소괄호가 없어도 튜플이 만들어진다는 점에 주목
# =============================================================

packed_values = 1, 2, 3, 4, 5
print(packed_values)  # (1, 2, 3, 4, 5)


# =============================================================
# [2] '*' 을 활용한 패킹 - 위치 인자를 튜플로 묶음
# - 함수를 '정의'할 때 사용
# - 전달된 인자 개수와 상관없이 모두 받아낼 수 있음
# =============================================================

# '*' 을 활용한 패킹 (함수 매개변수 작성 시)
def my_func(*args):
    print(args)  # (1, 2, 3, 4, 5)
    print(type(args))  # <class 'tuple'>


my_func(1, 2, 3, 4, 5)


# =============================================================
# [3] '**' 을 활용한 패킹 - 키워드 인자를 딕셔너리로 묶음
# - 별 하나(*)는 튜플, 별 둘(**)은 딕셔너리로 기억하면 쉬움
# =============================================================

# '**' 을 활용한 패킹 (함수 매개변수 작성 시)
def my_func2(**kwargs):
    print(kwargs)  # {'a': 1, 'b': 2, 'c': 3}
    print(type(kwargs))  # <class 'dict'>


my_func2(a=1, b=2, c=3)
