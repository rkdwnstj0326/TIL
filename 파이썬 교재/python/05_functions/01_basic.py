# =============================================================
# [1] 함수 정의와 docstring
# - def 함수명(매개변수): 형태로 정의
# - 함수 첫 줄의 """ """ 는 docstring => 함수의 설명서 역할
# - return : 값을 돌려주고 함수를 즉시 종료
# =============================================================


def make_sum(pram1, pram2):
    """이것은 두 수를 받아
    두 수의 합을 반환하는 함수입니다.
    >>> make_sum(1, 2)
    3
    """
    result = pram1 + pram2
    return result


# =============================================================
# [2] 함수 호출과 반환값 활용
# - 호출한 자리는 '반환된 값'으로 대체된다고 생각하면 이해가 쉬움
#   result = make_sum(100, 30) => result = 130
# =============================================================

# 함수 호출 및 반환 값 할당
result = make_sum(100, 30)
print(result)


# =============================================================
# [3] 반환값이 없는 함수는 None을 반환
# - '출력(print)'과 '반환(return)'은 완전히 다른 개념
# - print()는 화면에 보여줄 뿐, 돌려주는 값은 None
# - 초보자가 가장 많이 혼동하는 지점
# =============================================================

# print() 함수는 반환값이 없다.
value = print('hello world')
print(value)


# -------------------------------------------------------------
# return문이 아예 없는 함수도 마찬가지로 None을 반환
# -------------------------------------------------------------
def my_func():
    print('hello')


result = my_func()
print(result)
