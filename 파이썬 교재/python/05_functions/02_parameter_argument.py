# =============================================================
# 매개변수(Parameter) vs 인자(Argument)
# - 매개변수 : 함수를 '정의'할 때 값을 받기 위해 선언하는 이름
# - 인자     : 함수를 '호출'할 때 실제로 전달하는 값
# - 즉, 인자(a, b)의 값이 매개변수(x, y)에 복사되어 들어감
#   => a의 값 2가 x로, b의 값 3이 y로 전달됨
# - 함수 밖에서는 x, y라는 이름을 사용할 수 없음 (다음 scope에서 학습)
# =============================================================

def add_numbers(x, y):  # x와 y는 매개변수
    result = x + y
    return result


a = 2
b = 3

sum_result = add_numbers(a, b)  # a와 b는 인자
print(sum_result)  # 5
