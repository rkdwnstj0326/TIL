# ⚠️ [실행 주의] 이 파일은 그대로 실행하면 동작하지 않습니다.
#    [2], [3]번의 SyntaxError는 파이썬이 파일을 읽는 시점에 걸리므로
#    '파일 전체가 한 줄도 실행되지 않습니다.'
#    => [1]번을 확인할 때는 [2], [3]번을 주석 처리하고 실행할 것
# =============================================================

# =============================================================
# [1] global 기본 사용법
# - 함수 안에서 전역 변수의 '값을 변경'하려면 global 선언이 필요
# - 선언 없이 num += 1 을 하면 UnboundLocalError 발생
# - 다만 global은 코드 추적을 어렵게 만들어 실무에서는 지양
#   => 값을 받아 return 으로 돌려주는 방식이 권장됨
# =============================================================

num = 0  # 전역 변수


def increment():
    global num  # num를 전역 변수로 선언
    num += 1


print(num)  # 0

increment()

print(num)  # 1


# =============================================================
# [2] 주의사항 1 - global 선언보다 먼저 사용하면 안 됨
# - 함수 안에서 이미 그 이름을 사용한 뒤에 global을 선언하면 SyntaxError
# - global 선언은 함수 최상단에 위치해야 함
# =============================================================

# 'global' 키워드 주의사항 1 - global 키워드 선언 전에는 참조불가
num = 0


def increment():
    # SyntaxError: name 'num' is used prior to global declaration
    print(num)
    global num
    num += 1


# =============================================================
# [3] 주의사항 2 - 매개변수에는 global 사용 불가
# - 매개변수는 이미 '지역 변수'로 확정된 이름
# - 같은 이름을 전역이라고 다시 선언할 수 없으므로 SyntaxError
# =============================================================

# 'global' 키워드 주의사항 2 - 매개변수에는 global 키워드 사용불가
num = 0


def increment(num):
    # SyntaxError: name 'num' is parameter and global
    global num
    num += 1
