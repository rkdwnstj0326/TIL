# =============================================================
# 언패킹 (Unpacking) - 묶여 있는 값을 하나씩 풀어내는 것
# - 패킹과 정확히 반대 방향의 동작
# =============================================================

# =============================================================
# [1] 기본 언패킹
# - 왼쪽 변수 개수와 오른쪽 값 개수가 정확히 일치해야 함
#   => 개수가 맞지 않으면 ValueError
# =============================================================

packed_values = 1, 2, 3, 4, 5

# 언패킹
a, b, c, d, e = packed_values
print(a, b, c, d, e)  # 1 2 3 4 5


# =============================================================
# [2] '*' 을 활용한 언패킹 - 리스트/튜플을 위치 인자로 풀어 전달
# - 함수를 '호출'할 때 사용 (정의할 때 쓰는 패킹과 위치가 다름)
# - my_function(*names) == my_function('alice', 'jane', 'peter')
# =============================================================

# '*' 을 활용한 언패킹 (함수 인자 전달)
def my_function(x, y, z):
    print(x, y, z)


names = ['alice', 'jane', 'peter']
my_function(*names)  # alice jane peter


# =============================================================
# [3] '**' 을 활용한 언패킹 - 딕셔너리를 키워드 인자로 풀어 전달
# - 딕셔너리의 '키'가 매개변수 이름과 일치해야 함
# - my_function(**my_dict) == my_function(x=1, y=2, z=3)
# =============================================================

# '**'을 활용한 언패킹 (딕셔너리 -> 함수 키워드 인자)
def my_function(x, y, z):
    print(x, y, z)


my_dict = {'x': 1, 'y': 2, 'z': 3}
my_function(**my_dict)  # 1 2 3
