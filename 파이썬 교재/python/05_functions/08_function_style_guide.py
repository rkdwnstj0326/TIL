# =============================================================
# [1] 이름 짓기 - 함수명은 '동사 + 대상'으로 역할이 드러나게
# - 좋은 이름은 주석 없이도 무엇을 하는 함수인지 알려줌
# - 줄여 쓴 이름(p, t)은 작성할 땐 편하지만 읽을 땐 비용이 됨
# =============================================================

# Good
def calculate_total_price(price, tax):
    return price + (price * tax)


# Bad
def calc_price(p, t):
    return p + (p * t)


# =============================================================
# [2] 나쁜 설계 - 하나의 함수가 여러 책임을 떠안은 경우
# - 검사 + 저장 + 메일 발송이 한 함수에 섞여 있음
# - 문제점: 일부만 재사용할 수 없고, 테스트가 어렵고,
#           수정 시 관련 없는 기능까지 망가질 위험이 큼
# =============================================================

# 잘못된 설계 예시 (여러 책임이 섞인 함수)
def process_user_data(user_data):
    # 책임 1: 데이터 유효성 검사
    if len(user_data['password']) < 8:
        raise ValueError('비밀번호는 8자 이상이어야 합니다')

    # 책임 2: 비밀번호 암호화 및 저장
    user_data['password'] = hash_password(user_data['password'])
    db.users.insert(user_data)

    # 책임 3: 이메일 발송
    send_email(user_data['email'], '가입을 환영합니다!')


# =============================================================
# [3] 좋은 설계 - 하나의 함수는 하나의 일만 (단일 책임)
# - 각 함수가 작아져서 이름만 봐도 역할이 분명해짐
# - 개별 재사용/테스트가 가능해지고, 수정 범위가 좁아짐
# - 각 함수의 docstring이 곧 그 함수의 책임 정의가 됨
# =============================================================

# 올바른 설계 예시 (책임을 분리한 함수들)
def validate_password(password):
    """비밀번호 유효성 검사"""
    if len(password) < 8:
        raise ValueError('비밀번호는 8자 이상이어야 합니다')


def save_user(user_data):
    """비밀번호 암호화 및 저장"""
    user_data['password'] = hash_password(user_data['password'])
    db.users.insert(user_data)


def send_welcome_email(email):
    """환영 이메일 발송"""
    send_email(email, '가입을 환영합니다!')


# -------------------------------------------------------------
# 메인 함수는 '무엇을 어떤 순서로 하는지'만 보여주는 목차가 됨
# => 세부 구현은 각 함수 안으로 숨겨짐 (추상화)
# -------------------------------------------------------------

# 메인 함수에서 순차적으로 실행
def process_user_data(user_data):
    validate_password(user_data['password'])
    save_user(user_data)
    send_welcome_email(user_data['email'])
