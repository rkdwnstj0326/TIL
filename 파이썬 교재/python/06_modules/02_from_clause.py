# =============================================================
# [2] 주의 사항 - 다른 모듈의 같은 이름끼리도 충돌
# - 나중에 import한 쪽이 앞의 것을 덮어씀 (위에서 아래로 실행되므로)
# - import 문의 '순서'만으로 동작이 달라지는 상황이 만들어짐
#
# =============================================================


## 같은 이름인 경우 덮어쓰기 주의
from math import sqrt  # math.sqrt가 먼저 import됨

from my_math import sqrt  # my_math.sqrt가 math.sqrt를 덮어씀

result = sqrt(9)  # math.sqrt가 아닌 my_math.sqrt가 사용됨


# =============================================================
# [3] import * 는 별개의 문제
# - * 는 무엇을 가져왔는지 알 수 없음
# - * 는 모듈 안의 모든 이름을 한 번에 가져옴
# - 어떤 이름이 들어왔는지 코드만 봐서는 알 수 없으므로,
#   내가 만든 변수와 충돌해도 원인을 찾기 매우 어려움
#   => math의 자연상수 e가 있는 줄 모르고 e = 300을 쓰면 그대로 덮어씀
# =============================================================


