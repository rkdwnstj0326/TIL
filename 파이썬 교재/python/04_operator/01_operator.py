# =============================================================
# [1] 복합 연산자 (Augmented Assignment)
# - 연산과 재할당을 한 번에 하는 축약 표현
# - y -= 4 는 y = y - 4 와 같음
# - 주의: / 는 결과가 항상 실수(float), // 는 몫만 남는 정수 나눗셈
# =============================================================

# 복합 연산자
y = 10
y -= 4
# y = y - 4
print(y)  # 6

z = 7
z *= 2
print(z)  # 14

w = 15
w /= 4
print(w)  # 3.75

q = 20
q //= 3
print(q)  # 6


# =============================================================
# [2] 비교 연산자
# - 결과는 항상 True 또는 False (불리언)
# - == 는 '값'이 같은지 비교 / 대입 연산자 = 와 혼동 주의
# - 문자열 비교는 대소문자를 구분함
# =============================================================

# 비교 연산자
print(3 > 6)  # False

# == 연산자
print(2 == 2.0)  # True
print(2 != 2)  # False
print('HI' == 'hi')  # False
print(1 == True)  # True

# =============================================================
# [3] is 연산자 - '값'이 아닌 '동일한 객체인지'를 비교
# - == 는 값 비교 / is 는 정체(identity) 비교
# - 초보자가 가장 많이 헷갈리는 지점
# =============================================================

# is 비교 연산자
# SyntaxWarning: "is" with a literal. Did you mean "=="?
print(1 is True)  # False
print(2 is 2.0)  # False

# 왜 is 대신 ==를 사용해야 하나?
# 1(정수)과 True(불리언)는 다른 객체이다.
print(1 is True)  # False
# 1과 True의 '값'은 논리적으로 같다.
print(1 == True)  # True

# 2(정수)와 2.0(실수)은 다른 객체이다.
print(2 is 2.0)  # False
# 2와 2.0의 '값'은 논리적으로 같다.
print(2 == 2.0)  # True

# -------------------------------------------------------------
# is의 올바른 사용처: None / True / False 처럼
# 프로그램 전체에서 단 하나만 존재하는 '싱글턴 객체' 비교
# -------------------------------------------------------------

# is 연산자는 언제 사용하는가?
# 싱글턴 객체 비교할 때
x = None
# 권장
if x is None:
    print('x는 None입니다.')
# 비권장
if x == None:
    print('x는 None입니다.')


x = True
y = True
print(x is y)  # True
print(True is True)  # True
print(False is False)  # True
print(None is None)  # True


# =============================================================
# [4] 논리 연산자
# - and : 둘 다 참이어야 참
# - or  : 하나만 참이어도 참
# - not : 참/거짓을 뒤집음 (0, '', [] 등은 거짓으로 취급)
# =============================================================

# 논리 연산자
print(True and False)  # False
print(True or False)  # True
print(not True)  # False
print(not 0)  # True


# =============================================================
# [5] 논리 연산자 + 비교 연산자 조합
# - 비교 연산이 먼저 True/False로 바뀐 뒤 논리 연산이 수행됨
# - 괄호로 묶으면 의도가 명확해지고 우선순위 실수를 줄일 수 있음
# =============================================================

# 논리 연산자 & 비교 연산자
num = 15
result = (num > 10) and (num % 2 == 0)
print(result)  # False

name = 'Alice'
age = 25
result = (name == 'Alice') or (age == 30)
print(result)  # True
