# =============================================================
# 멤버십 연산자 (Membership Operator)
# - in / not in : 특정 값이 포함되어 있는지 확인, 결과는 True/False
# - 문자열, 리스트, 튜플, 세트, 딕셔너리 등에 사용 가능
#   (딕셔너리는 '키'를 기준으로 확인)
# =============================================================

# 멤버십 연산자

word = 'hello'
numbers = [1, 2, 3, 4, 5]

# -------------------------------------------------------------
# [1] in - 포함되어 있으면 True
# -------------------------------------------------------------
print('h' in word)  # True
print('z' in word)  # False

# -------------------------------------------------------------
# [2] not in - 포함되어 있지 않으면 True
# -------------------------------------------------------------
print(4 not in numbers)  # False
print(6 not in numbers)  # True
