# =============================================================
# 단축 평가 (Short-circuit Evaluation)
# - and / or 는 True나 False가 아니라 '판단이 끝난 시점의 값'을 반환
# - and : 거짓인 값을 만나면 즉시 그 값을 반환, 끝까지 참이면 마지막 값
# - or  : 참인 값을 만나면 즉시 그 값을 반환
#
# [실습] 각 결과를 먼저 예측해보고, 실행해서 확인할 것
#        빈 문자열('')은 거짓으로 취급된다는 점이 힌트
# =============================================================

# 단축 평가

# -------------------------------------------------------------
# [1] 둘 다 값이 있는 경우
# -------------------------------------------------------------
# 준비물 1: 내용이 있는 문자열
item1 = '지도'
# 준비물 2: 내용이 있는 문자열
item2 = '나침반'
result = item1 and item2
print(f'최종적으로 챙긴 물건: {result}')
# >> 최종적으로 챙긴 물건: ??

# -------------------------------------------------------------
# [2] 뒤쪽이 거짓인 경우
# -------------------------------------------------------------
item1 = '지도'
# 준비물 2: 내용이 없는 빈 문자열
item2 = ''
result = item1 and item2
print(f'최종적으로 챙긴 물건: "{result}"')
# >> 최종적으로 챙긴 물건: ??


# -------------------------------------------------------------
# [3] 앞쪽이 거짓인 경우 => 뒤쪽은 아예 평가하지 않음
# -------------------------------------------------------------
# 준비물 1: 내용이 없는 빈 문자열
item1 = ''
item2 = '나침반'
result = item1 and item2
print(f'최종적으로 챙긴 물건: "{result}"')
# >> 최종적으로 챙긴 물건: ??
