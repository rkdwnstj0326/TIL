# =============================================================
# 시퀀스형 연산자 (Sequence Operator)
# - + : 두 시퀀스를 이어 붙임 (연결)
# - * : 시퀀스를 정해진 횟수만큼 반복
# - 주의: 같은 종류의 시퀀스끼리만 + 연산이 가능
#         'hi' + 5 처럼 다른 자료형과 더하면 TypeError
# =============================================================

# 시퀀스형 연산자

# -------------------------------------------------------------
# [1] 문자열
# -------------------------------------------------------------
print('Gildong' + ' Hong')  # Gildong Hong
print('hi' * 5)  # hihihihihi

# -------------------------------------------------------------
# [2] 리스트 - 문자열과 동일한 규칙으로 동작
# -------------------------------------------------------------
print([1, 2] + ['a', 'b'])  # [1, 2, 'a', 'b']
print([1, 2] * 2)  # [1, 2, 1, 2]
